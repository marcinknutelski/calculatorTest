package pl.edu.agh.mwo.calculator;

public class Calculator {
    public int sum(int x, int y) {
        return x+y;
    }

    public int sub(int x, int y) {
        return x-y;
    }

    public int multiply(int x, int y) {
        return x*y;
    }

    public int divide(int x, int y) {
        return x/y;
    }
}
